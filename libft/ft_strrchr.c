#include "libft.h"

char	*ft_strrchr(const char *str, int n)
{
	int				i;
	unsigned char	c;

	c = (unsigned char)n;
	i = ft_strlen(str);
	if (c == 0)
		return ((char *)str + i);
	while (i >= 0)
	{
		if (str[i] == c)
			return ((char *)str + i);
		i--;
	}
	return (NULL);
}
