#include "libft.h"

char	*ft_strdup(const char *str)
{
	char	*new;
	size_t	i;
	size_t	size;

	i = 0;
	size = ft_strlen(str);
	new = malloc(sizeof(char) * (size + 1));
	if (!new)
		return (NULL);
	while (str[i] != '\0')
	{
		new[i] = str[i];
		i++;
	}
	new[i] = '\0';
	return (new);
}
